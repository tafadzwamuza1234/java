import java.io.*;
class Arithmetic
{
    public static void main(String args[])
    {
        int a=20,b=10;
        int sum=a+b;
        int diff=a-b;
        int div=a/b;
        int mult=a*b;
        
        System.out.println(sum);
        System.out.println(diff);
        System.out.println(div);
        System.out.println(mult);
    }
}
